# Example 1: Unpacking a List
my_list = [1, 2, 3]
a, b, c = my_list

print(f'a: {a}, b: {b}, c: {c}')

# Output:
# a: 1, b: 2, c: 3

# Example 2: Unpacking a Tuple
my_tuple = ('apple', 'banana', 'cherry')
fruit1, fruit2, fruit3 = my_tuple

print(f'fruit1: {fruit1}, fruit2: {fruit2}, fruit3: {fruit3}')

# Output:
# fruit1: apple, fruit2: banana, fruit3: cherry
